// Parameters and Arguments :
//      The input that given into the function is "PARAMETERS".
//      The value that set to that input (parameter) is known as "ARGUMENTS".

// Syntax :
        function fatherName (father){
                alert("Is your's father name is " + father)   // Setting the parameters
            }

        fatherName("Kandan");                                 // Setting the Arguments

// For Example :
        function tailoringWork (shirts,pants){
            alert("your's "+shirts+" shirts and your's "+pants+" pants is ready now !!!"); // Setting parameters
        }

        tailoringWork(4,2);                                                                // Setting arguments