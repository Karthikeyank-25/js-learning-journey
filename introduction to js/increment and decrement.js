// Increment and Decrement :
//          It is one of the technique to incrementing (increasing value) and decrementing (decreasing value) value from the give value (number).

// Syntax : (one value incrementing and decrementing)
            a++ // (for incrementing)
            a-- // (for decrementing)

// Syntax : (multiple value incrementing and decrementing)
            x += y; // (for incrementing)
            x -= y; // (for decrementing)

// For Example :
            x = 23;
            y = 72;
            x += y; // (incrementing)
            x -= y; // (decrementing)