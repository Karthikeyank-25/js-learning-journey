// Manipulating html attributes :
//              It is used to manipulates the attributes of the html tags by using javaScript property.

// Syntax :
                document.firstElementChild.attributes;                              // Check the available attributes of html tag
                document.firstElementChild.getAttribute(".first");                  // Getting the attributes of html tag
                document.firstElementChild.setAttribute(".first,www.google.com");   // Setting the attribute value to the tag