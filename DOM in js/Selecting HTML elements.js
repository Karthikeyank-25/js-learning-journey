// Selecting Html Elements :
//          It is used to select html elements in the documents by differet methods.

// Syntax :
                    - document.getElementById("");            // select by ID name
                    - document.getElementsByClassName("");    // select by Class name
                    - document.getElementsByTagName("");      // select by Tag name
                    - document.querySelector("");             // select by guery like (.class,#ID) & (li a h1)
                    - document.querySelectorAll("");          // select all element present within that query